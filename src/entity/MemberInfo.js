export default class MemberInfo {
  get email() {
    return this._email;
  }
  set email(value) {
    this._email = value;
  }

  get gender() {
    return this._title;
  }
  set gender(value) {
    this._title = value;
  }

  get firstname() {
    return this._firstname;
  }
  set firstname(value) {
    this._firstname = value;
  }
  
  get lastname() {
    return this._lastname;
  }
  set lastname(value) {
    this._lastname = value;
  }

  get password() {
    return this._password;
  }
  set password(value) {
    this._password = value;
  }

  get birthdate() {
    return this._birthdate;
  }
  set birthdate(value) {
    this._birthdate = value;
  }

  get birthmonth() {
    return this._birthmonth;
  }
  set birthmonth(value) {
    this._birthmonth = value;
  }

  get birthyear() {
    return this._birthyear;
  }
  set birthyear(value) {
    this._birthyear = value;
  }

}